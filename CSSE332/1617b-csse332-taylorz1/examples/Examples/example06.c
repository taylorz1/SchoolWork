#include <stdio.h>

struct birthday {
  int month;
  int day;
  int year;
};  // Note the semi-colon

int main(int argc, char *argv[]) {
  /* - new() is not needed! 
     - to access, use member notation like for Java object! */
  struct birthday mybday;

  mybday.day = 1;
  mybday.month = 1;
  mybday.year = 1977;

  printf("I was born on %d/%d/%d\n", mybday.day, mybday.month, mybday.year);
  return 0;
}
